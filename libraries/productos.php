function productos_destacados() : array {
    $resultado = [];
    $catalogo = catalogo_completo();

    foreach ($catalogo as $p) {
        if($p['destacado'] ?? false){
            $resultado[] = $p;
    }
    }

    return $resultado;

}

function catalogo_todos_juegos() : array {
    $resultado = [];
    $catalogo = catalogo_completo();

    foreach ($catalogo as $p) {
        $resultado[] = $p;
    }

    return $resultado;

}

function catalogo_x_plataformac(string $personaje) : array {
    $resultado = [];
    $catalogo = catalogo_completo();

    foreach ($catalogo as $p) {
        if($p["plataformac"] == $personaje){
            //agregar elementos al array
             $resultado[] = $p;
        }
    }

    return $resultado;

}


function producto_x_id(int $idProducto){
    $catalogo = catalogo_completo();

    foreach ($catalogo as $p) {
         if($p['id'] == $idProducto){
               return $p;
       }
    }

    return null;

}







